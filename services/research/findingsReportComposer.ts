/**
 * findingsReportComposer — the SINGLE, pure, deterministic composer for the
 * metaMe Invariant Research Lab Findings Report. Used by BOTH the live-draft
 * client tab (ExperimentReportTab) and the canonical server regeneration
 * (services/research/reportComposition.ts) so there is exactly ONE composition
 * of record — no drift, and no LLM latency (the canonical regenerate previously
 * called a sovereign model and 504'd at the ~30s gateway envelope).
 *
 * Composition obeys the platform's own laws (dogfooding EXP-002): experiments
 * are emitted in canonical EXPERIMENT_REGISTRY order grouped under their series
 * (sequential narrative law), and the introduction is generated from the same
 * registry + record (global coherence) — a single ordered document, never a
 * narrated head with an appended tail. An experiment with no runs yet but an
 * authored body (e.g. EXP-005) appears in its correct slot as publication
 * pending; a published run outside the registry is preserved under a labelled
 * "Further experiments" block (registry drives ORDER, never suppression).
 *
 * Pure: no React, no DB, no crypto, no provider calls — safe to import from
 * both a client component and a server route.
 */

import { EXPERIMENT_REGISTRY, SERIES_REGISTRY, type ResearchExperiment } from "@/types/research";

/** One canonical run, provider-agnostic (both call sites map their row to this). */
export interface ReportRun {
  provider: string;
  model: string;
  aggregates: Record<string, unknown>;
  contentHash: string;
  receiptStatus?: string | null;
  /** ISO timestamp. */
  createdAt: string;
}

function aggLine(agg: Record<string, unknown>): string {
  return Object.entries(agg ?? {})
    .filter(([k]) => k !== "note")
    .map(([k, v]) => `${k}: ${typeof v === "number" ? Math.round(v * 1000) / 1000 : String(v)}`)
    .join(" · ");
}

function runsTable(rows: ReportRun[]): string {
  if (rows.length === 0) return "_No canonical runs published yet for this experiment._\n";
  const lines = [
    "| Published | Provider / model | Aggregates | Content commitment (sha256) | DVN |",
    "|---|---|---|---|---|",
  ];
  for (const r of rows) {
    lines.push(
      `| ${new Date(r.createdAt).toISOString().slice(0, 10)} | ${r.provider} / ${r.model} | ${aggLine(r.aggregates) || "—"} | \`${r.contentHash.slice(0, 16)}…\` | ${r.receiptStatus ?? "local"} |`,
    );
  }
  return lines.join("\n") + "\n";
}

/**
 * Objective, data-driven EXP-006 → EXP-006A narrative BODY (no heading, no runs
 * table — the composition loop supplies both). The exact-set F1 is a LOWER BOUND
 * distorted by abstraction mismatch, not a competence score.
 */
function exp006Body(rows: ReportRun[]): string {
  const pctf = (v: number | null) => (v === null ? "—" : `${Math.round(v * 100)}%`);
  const framing = `**Aim.** Move from *outcome* evaluation to *mechanism* evaluation: can the invariant projection engine identify the governing invariant set for an intent **independently of a human-authored reference** — i.e. did it select the right constitutional substrate before reasoning began?

**Method (Stage A).** For each intent in an independently-generated reference set (CIRS), the sovereign, invariant-aware router predicts the projection; it is scored against the *blind* reference; every disagreement is classified as an Invariant Delta. The reference is generated by a different reasoning stage/provider than the predictor, so the deltas are real cross-model disagreements, not self-agreement.`;

  const successor = `**EXP-006A — the successor metric.** Topology / abstraction-aware scoring reclassifies each disagreement as *vocabulary* (normalisation), *abstraction* (predicted subsumes a reference item — same family, different level), *omission* (a genuine gap), or *redundant*. The subsumption signal is **graph-truth first**: it reads the invariant graph's \`specializes\`/\`generalizes\` edges (built by the Discovery Engine's Compare + parent-linking) and falls back to an embedding proxy only where a label is not yet a registry node — reporting the two separately so they are never conflated. Projection Fidelity becomes a composite (structural + causal coverage + minimality), so a low lexical F1 can coexist with high causal agreement.

**Engine roles EXP-006 clarified.** The **Invariant Resolution Engine** projects an intent *down* to a minimal invariant set; the **Discovery Engine's Compare** compresses many sub-domain sets *up* into earned higher-order invariants; **EXP-006A** scores a projection *against* that same graph. They are inverse operations over one invariant graph — and EXP-006 plateaued precisely because the Discovery Engine had outgrown a name-matching metric.

**For anyone running EXP-006:** publish the lexical baseline, frame it as *lexical agreement with a known abstraction ceiling* (not "the engine scores 0.4X"), then **follow on to EXP-006A** for the causal-agreement metric.`;

  if (rows.length === 0) {
    return `${framing}\n\n**Status.** Run complete; canonical publication pending. Superseded as the *fidelity metric* by **EXP-006A** (topology / abstraction-aware).\n\n${successor}`;
  }
  const latest = rows.slice().sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())[0];
  const agg = latest.aggregates ?? {};
  const p = typeof agg.meanPrecision === "number" ? (agg.meanPrecision as number) : null;
  const r = typeof agg.meanRecall === "number" ? (agg.meanRecall as number) : null;
  const f1 = p !== null && r !== null && p + r > 0 ? (2 * p * r) / (p + r) : null;
  const deltas = typeof agg.deltaCount === "number" ? (agg.deltaCount as number) : null;

  return `${framing}

**Measured (latest canonical run, ${new Date(latest.createdAt).toISOString().slice(0, 10)}, ${latest.provider} / ${latest.model}).** Exact-set *lexical* fidelity: precision ${pctf(p)} · recall ${pctf(r)} · F1 ${pctf(f1)}${deltas !== null ? ` · ${deltas} Invariant Deltas classified` : ""}.

**Finding — read objectively, this F1 is a lower bound, not a competence score.** The exact-set metric measures *lexical* agreement, and two facts make the raw number a distorted floor:
1. The sovereign router projects invariants **one abstraction level higher** than the flat reference — e.g. for an authenticated-delegation intent it returns \`authentication · delegation · security\`, which *subsume* the reference's \`token-validation · scope-limitation · secure-access\`. Exact scoring reads that as zero overlap; it is an abstraction mismatch, not a projection failure.
2. The semantic-retrieval baseline that looks competitive draws from the *reference's own vocabulary* (it retrieves labels resembling the answer key), while the sovereign router projects independently. Different tasks; the comparison flatters retrieval.

Invariant selection therefore cannot be evaluated as flat set-overlap. **EXP-006 is frozen here as the last lexical-fidelity record** — it has done its job: it established that the scientific question is *did the engine select the correct invariant family, at the correct abstraction level, with a minimal sufficient set?*

${successor}`;
}

