/** Framed auto-section for an experiment with published runs but no authored prose. */
function framedBody(exp: ResearchExperiment): string {
  return `**Aim.** ${exp.hypothesis}

_Auto-summarised from the experiment registry; the full protocol lives in the repository (\`${exp.protocolRef}\`)._`;
}

/** Section title — EXP-006 signals its EXP-006A succession in the heading. */
function titleFor(exp: ResearchExperiment): string {
  return exp.id === "EXP-006" ? `EXP-006 → EXP-006A — ${exp.family}` : `${exp.id} — ${exp.family}`;
}

export interface FindingsReportInput {
  /** Published canonical runs grouped by experiment id. */
  runsByExp: Record<string, ReportRun[]>;
  /** Composition timestamp (the "Report generated" date). */
  now: Date;
}

/** Compose the whole Findings Report markdown deterministically. */
export function composeFindingsReport({ runsByExp, now }: FindingsReportInput): string {
  const byExp = (id: string): ReportRun[] => runsByExp[id] ?? [];
  const runCount = (id: string) => byExp(id).length;
  const memberStatus = (id: string) => {
    const c = runCount(id);
    return c > 0 ? `**validated** (${c} canonical run${c > 1 ? "s" : ""})` : "run complete; canonical publication pending";
  };

  const allRuns = Object.values(runsByExp).flat();
  const latestRunDate =
    allRuns.length > 0
      ? new Date(Math.max(...allRuns.map((r) => new Date(r.createdAt).getTime()))).toISOString().slice(0, 10)
      : null;
  const distinctExpCount = Object.keys(runsByExp).filter((id) => (runsByExp[id]?.length ?? 0) > 0).length;
  const totalRuns = allRuns.length;

  // Sequential composition — one ordered spine (canonical registry order,
  // grouped by series). Included if authored OR has any published run.
  const included = EXPERIMENT_REGISTRY.filter((e) => AUTHORED_BODIES[e.id] || runCount(e.id) > 0);
  const seriesInOrder = SERIES_REGISTRY.filter((s) => included.some((e) => e.seriesId === s.id));

  let n = 0;
  const num = () => (n += 1);
  const introNo = num();
  const trustNo = num();

  const programmeMap = seriesInOrder
    .map((s) => {
      const members = included
        .filter((e) => e.seriesId === s.id)
        .map((e) => `  - **${e.id}** — ${e.family}: ${memberStatus(e.id)}`)
        .join("\n");
      return `**${s.name}.** ${s.claim}\n${members}`;
    })
    .join("\n\n");

  let lastSeries = "";
  const expSections = included
    .map((exp) => {
      let banner = "";
      if (exp.seriesId !== lastSeries) {
        lastSeries = exp.seriesId;
        const s = SERIES_REGISTRY.find((x) => x.id === exp.seriesId);
        if (s) banner = `---\n\n### ${s.name}\n*${s.claim}*\n\n`;
      }
      const body = AUTHORED_BODIES[exp.id] ? AUTHORED_BODIES[exp.id](byExp(exp.id)) : framedBody(exp);
      return `${banner}## ${num()}. ${titleFor(exp)}\n\n${body}\n\n**Canonical runs:**\n${runsTable(byExp(exp.id))}`;
    })
    .join("\n");

  // No-data-loss: published runs outside the pinned registry still appear.
  const registeredIds = new Set(EXPERIMENT_REGISTRY.map((e) => e.id));
  const orphanIds = Object.keys(runsByExp)
    .filter((id) => (runsByExp[id]?.length ?? 0) > 0 && !registeredIds.has(id))
    .sort();
  const orphanSections = orphanIds
    .map((id, i) => {
      const banner =
        i === 0
          ? "---\n\n### Further experiments\n*Published runs outside the pinned experiment registry — surfaced so no canonical record is dropped.*\n\n"
          : "";
      return `${banner}## ${num()}. ${id}\n\n**Aim.** _Not in the pinned registry; see the experiment's protocol in the repository._\n\n**Canonical runs:**\n${runsTable(byExp(id))}`;
    })
    .join("\n");

  const conclusionsNo = num();
  const limitationsNo = num();
  const inviteNo = num();

  return `# The metaMe Invariant Research Lab — Findings Report (Draft)

**Chrysalis Foundation / Invariant Intelligence · AigentZ platform**
**Status: CONFIDENTIAL DRAFT for strategic partners — not for publication or redistribution**
**Report generated: ${now.toISOString().slice(0, 10)} · Latest canonical run: ${latestRunDate ?? "none yet"} · ${totalRuns} published run${totalRuns === 1 ? "" : "s"} across ${distinctExpCount} experiment${distinctExpCount === 1 ? "" : "s"}**
*Composed sequentially by the platform's own narrative-sequencing + coherence composition laws (EXP-002): experiments appear in canonical series order and the introduction reflects the full record — this is a single ordered document, not a narrated head with an appended tail. Run tables render live from the canonical record.*

---

## ${introNo}. Introduction — what is being validated

The platform's core knowledge primitive is the **invariant**: a versioned,
provenance-bearing statement of validated knowledge (e.g. *"Authority follows
standing"*), stored in a graph, classified by an ontology, and composed by
per-class composition laws into experiences — articles, reports, stories,
video, agent behaviour. The claim under test is **reasoning compression**:
that a curated collection of validated invariants functions as compressed,
reusable expertise which survives transformation across modalities, survives
composition across time, and measurably reduces the cost of reasoning while
improving its fidelity.

The research programme is organised into **series**, each isolating a distinct
property of the invariant substrate. This report reflects the **full canonical
record to date** — every series with a published run, and every member
experiment in its canonical sequence (an experiment whose run is complete but
not yet published is shown in place as *publication pending*, never omitted):

${programmeMap}

Every run is a separate experiment instance — provider and model are recorded
with each result and cross-model rows are never merged.

## ${trustNo}. Trust model — why the numbers are auditable

Every published run stores its **exact results JSON** and a sha256 content
commitment over that exact text, anchored via a DVN-anchorable
(\`experiment_result_published\`) receipt on the platform's Decentralised
Verification Network pipeline. Verification is trustless and mechanical:
recompute sha256 over the stored text and compare with the anchored hash —
the platform's Results interface does this in-browser, taking no server
assertion on faith. The hashes in the data tables below are those
commitments.

${expSections}
${orphanSections ? orphanSections + "\n" : ""}---

## ${conclusionsNo}. Cross-cutting conclusions (current)

1. **One primitive, validated along orthogonal axes across several series.**
   Semantic fidelity across modalities (FVS), temporal fidelity across
   sequential composition (FVS), reasoning-cost reduction (FVS), constitutional
   operation under a sovereign provider and across interchangeable providers
   (Platform Sovereignty), and mechanism-level projection fidelity (Invariant
   Intelligence) — the same invariant substrate examined from independent
   directions, each a separate measurable property.
2. **Constitutional restraint is a distinct, measurable property.** Refusing
   to derive what the knowledge does not contain (15/15 probes in EXP-001) is
   separable from avoiding false assertions — and it is the property that
   makes invariant-grounded systems auditable.
3. **Composition is where both failures and validations live.** Every defect
   found by the series lived in an *interaction between fields* (a narrative
   beat lost in segment mapping; audio overrunning a segment boundary), never
   in a single component — and the validators built on that principle caught
   a real defect before a single frame was generated.
4. **Sequence is scored, not validated.** The reversed-order control (EXP-002)
   showed temporal coherence is a graded field over orderings with the designed
   sequence as its maximum — opening constrained resequencing (remix as an
   alternative coherent trajectory through the same invariant space) as a
   legitimate, scoreable operation. *This report is composed under that same
   law — its section order is the coherence maximum over the experiment set.*

## ${limitationsNo}. Limitations, stated plainly

- Single-model runs for the foundational legs (one OSS model for the text
  legs; one video provider) — those deltas are within-model; constants are not
  yet universals. Cross-model replication is the first scale-up step. (The
  sovereignty series already spans multiple providers by design.)
- The judge is a model; machine verdicts required human adjudication (both
  instructive, both recorded).
- EXP-002's formal independent-evaluator pass is open; current findings are
  operator-evaluated with frame evidence.
- The foundational collection was authored by the platform's own constitutional
  process — task-collection affinity is by design; cross-domain generalization
  is under active test (the Discovery Engine's domain-ladder + cross-domain
  comparison), not yet concluded.
- EXP-006's exact-set metric is a **lexical lower bound** with a known
  abstraction ceiling; its causal-agreement successor (EXP-006A) is the metric
  to read for projection competence.

## ${inviteNo}. What we're inviting partners to do

The next tier of rigor needs scale we intend to reach with partners: larger
task sets, multiple judge models, cross-domain collections, more permutation
coverage for the temporal-coherence field map (EXP-002b), and independent
replication of the initialization and sovereignty deltas. The full experiment
protocols, artifacts, and raw result records are available on request under the
same confidentiality.

---
*Draft report — composed deterministically from the canonical results record at
regeneration time. Sections are in canonical series/experiment order; the
introduction and data tables update automatically as runs are published.*
`;
}
